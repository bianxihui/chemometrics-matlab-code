function r = rms(y)
% rms -- Return Root Mean Square of Signal
%  Usage
%    r = rms(y)
%  Inputs
%    y    signal
%  Outputs
%    r    sqrt( mean ( y .^2 ))
%
r = sqrt( mean ( y .^2 ));
    
    
